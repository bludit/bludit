$_draftPosts = array();
$_scheduledPosts = array();
foreach($posts as $Post)
{
	if($Post->draft()) {
		array_push($_draftPosts, $Post);
	}
	elseif($Post->scheduled()) {
		array_push($_scheduledPosts, $Post);
	}
}

$_draftPages = array();
foreach($pages as $Page)
{
	if(!$Page->published()) {
		array_push($_draftPages, $Page);
	}
}
